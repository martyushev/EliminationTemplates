    P1 = data{1};
    P2 = data{2};
    P3 = data{3};
    x1 = data{4};
    x2 = data{5};
    x3 = data{6};

    C = zeros(3,24);

    C(28) = -P1(3)^2-P1(4)^2;
    C(26) = P1(4)^2+P1(3)^2;
    C(46) = -P1(5)^2-P1(6)^2;
    C(39) = P1(5)^2+P1(6)^2;
    C(70) = -P1(7)^2-P1(8)^2;
    C(14) = -P2(1)^2-P2(2)^2;
    C(16) = P2(1)^2+P2(2)^2;
    C(44) = -P2(5)^2-P2(6)^2;
    C(36) = P2(5)^2+P2(6)^2;
    C(68) = -P2(8)^2-P2(7)^2;
    C(1) = P3(1)^2+P3(2)^2;
    C(5) = P3(3)^2+P3(4)^2;
    C(9) = P3(5)^2+P3(6)^2;
    C(40) = -2*P1(3)*P1(5)-2*P1(4)*P1(6);
    C([27,38]) = P1(3)*P1(5)+P1(4)*P1(6);
    C(58) = -2*P1(3)*P1(7)-2*P1(4)*P1(8);
    C(56) = P1(4)*P1(8)+P1(3)*P1(7);
    C(64) = -2*P1(5)*P1(7)-2*P1(6)*P1(8);
    C(57) = P1(5)*P1(7)+P1(6)*P1(8);
    C(32) = -2*P2(1)*P2(5)-2*P2(2)*P2(6);
    C([18,34]) = P2(1)*P2(5)+P2(2)*P2(6);
