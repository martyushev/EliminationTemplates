    C(25) = x1(2)*P1(4)-P1(1)*P1(3)+x1(1)*P1(3)-P1(2)*P1(4);
    C(37) = x1(2)*P1(6)-P1(1)*P1(5)+x1(1)*P1(5)-P1(2)*P1(6);

    C = C./repmat(sqrt(sum(C.*conj(C),2)),1,size(C,2));

end