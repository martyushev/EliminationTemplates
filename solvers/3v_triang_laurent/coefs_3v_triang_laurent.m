    C(11) = P3(3)*P3(7)-P3(3)*x3(1)+P3(4)*P3(8)-P3(4)*x3(2);
    C(12) = P3(5)*P3(7)-P3(5)*x3(1)+P3(6)*P3(8)-P3(6)*x3(2);
