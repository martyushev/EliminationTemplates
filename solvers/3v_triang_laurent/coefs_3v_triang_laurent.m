    C(52) = P2(1)*P2(7)+P2(2)*P2(8);
    C(62) = -2*P2(5)*P2(7)-2*P2(6)*P2(8);
    C(54) = P2(5)*P2(7)+P2(6)*P2(8);
    C([2,4]) = P3(1)*P3(3)+P3(2)*P3(4);
    C([3,7]) = P3(1)*P3(5)+P3(2)*P3(6);
    C([6,8]) = P3(3)*P3(5)+P3(4)*P3(6);
    C(24) = P1(1)*P1(5)-x1(1)*P1(5)+P1(2)*P1(6)-x1(2)*P1(6);
    C(55) = -P1(1)*P1(7)+x1(1)*P1(7)-P1(2)*P1(8)+x1(2)*P1(8);
    C(17) = -P2(1)*P2(3)+P2(1)*x2(1)-P2(2)*P2(4)+P2(2)*x2(2);
    C(19) = P2(1)*P2(3)-P2(1)*x2(1)+P2(2)*P2(4)-P2(2)*x2(2);
    C(35) = -P2(3)*P2(5)+x2(1)*P2(5)-P2(4)*P2(6)+x2(2)*P2(6);
    C(21) = P2(3)*P2(5)-x2(1)*P2(5)+P2(4)*P2(6)-x2(2)*P2(6);
    C(53) = -P2(3)*P2(7)+x2(1)*P2(7)-P2(4)*P2(8)+x2(2)*P2(8);
