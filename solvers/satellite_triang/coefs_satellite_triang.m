    C = zeros(3,20);
    C([58,55,46,28,52,43,25,37,19,10,49,40,22,34,16,7,31,13,4,1]) = data{1};
    C([59,56,47,29,53,44,26,38,20,11,50,41,23,35,17,8,32,14,5,2]) = data{2};
    C([60,57,48,30,54,45,27,39,21,12,51,42,24,36,18,9,33,15,6,3]) = data{3};

