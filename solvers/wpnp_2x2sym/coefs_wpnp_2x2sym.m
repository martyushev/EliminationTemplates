function C = coefs_wpnp_2x2sym(data)

    A = data{1};
    B = data{2};

    C = zeros(4,16);

    s11 = A(1)*B(1);
    s24 = A(2)*B(4);
    s12 = A(1)*B(2);
    s23 = A(2)*B(3);
    s35 = A(3)*B(5);
    s36 = A(3)*B(6);
    t1 = A(1)^2;
    t2 = A(2)^2;

    C([8,27,34,37]) = 8*A(3)^2;
    C([3,13,32,46]) = 4*(t2-t1);
    C([11,18,24,41]) = 4*(t1+t2);
    C([60,63]) = -s35-1i*s36;
    C([50,53]) = -s35+1i*s36;
    C(64) = -s11+s24-1i*s12-1i*s23;
    C(49) = -s11+s24+1i*s12+1i*s23;
    C(54) = s11+s24-1i*s12+1i*s23;
    C(59) = s11+s24+1i*s12-1i*s23;

    C = C./repmat(sqrt(sum(C.*conj(C),2)),1,size(C,2));

end