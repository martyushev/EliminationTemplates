function C = coefs_wpnp_2x2sym(data)

    A = data{1};
    B = data{2};

    C = zeros(4,16);

    C([14,24,31,33]) = 8*A(3)^2;
    C([3,5,38,48]) = -4*A(1)^2+4*A(2)^2;
    C([12,17,26,43]) = 4*A(1)^2+4*A(2)^2;
    C([56,59]) = -1i*A(3)*B(6)-A(3)*B(5);
    C([50,61]) = 1i*A(3)*B(6)-A(3)*B(5);
    C(60) = -1i*A(1)*B(2)-1i*A(2)*B(3)-A(1)*B(1)+A(2)*B(4);
    C(62) = -1i*A(1)*B(2)+1i*A(2)*B(3)+A(1)*B(1)+A(2)*B(4);
    C(55) = 1i*A(1)*B(2)-1i*A(2)*B(3)+A(1)*B(1)+A(2)*B(4);
    C(49) = 1i*A(1)*B(2)+1i*A(2)*B(3)-A(1)*B(1)+A(2)*B(4);

    C = C./repmat(sqrt(sum(C.*conj(C),2)),1,size(C,2));

end