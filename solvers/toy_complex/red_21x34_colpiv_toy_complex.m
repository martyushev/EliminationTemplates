    M = sparse(ix,jx,C(kx),21,34);

    [L,~,P] = lu(M(:,1:2));
    P = P';
    M = [P*L P(:,3:end)]\M(:,3:end);
    M = full(M(3:end,:));

    [L,U,P] = lu(M(:,1:11));
    P = P';
    M = [P*L P(:,12:end)]\M(:,12:end);
