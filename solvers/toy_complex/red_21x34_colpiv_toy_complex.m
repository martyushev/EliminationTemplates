    [Q,R,P] = qr(M2(:,1:end-4));
    A = U\[M1(:,1:end-4)*P M1(:,end-3:end)];
    B = R(:,1:8)\[R(:,9:end) Q'*M2(:,end-3:end)];
    M = [-A(:,end-12:end)+A(:,1:end-13)*B; -B];

