function w = get_w(C,x,y,z)

    m1 = [x^3*z;x^2*z*y;x*z*y^2;y^3*z;x^3;y*x^2;y^2*x;y^3;x^2*z;x*z*y;y^2*z;x^2;x*y;y^2;z*x;y*z;x;y;z;1];
    m2 = [x^3*z^3;x^2*z^3*y;y^2*z^3*x;y^3*z^3;x^3*z^2;y*x^2*z^2;x*z^2*y^2;y^3*z^2;x^2*z^3;x*z^3*y;y^2*z^3;x^2*z*y;x*z*y^2;y^3*z;x^2*z^2;x*z^2*y;z^2*y^2;x*z^3;z^3*y;y^2*x;y^3;x^2*z;x*z*y;y^2*z;x*z^2;y*z^2;z^3;x*y;y^2;z*x;y*z;z^2;x;y;z;1];
    A = [C(3:end,[5:8,16:21,23,31:33,36,38,44,45,48,52])*m1,C(3:end,[1:4,9:15,22,24:30,34,35,37,39:43,46,47,49:51,53:56])*m2];

    [~,~,V] = svd(A);
    w = V(3)/V(4);

end