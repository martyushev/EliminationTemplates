function [C,U,dU] = coefs_wpnp_2x2sym(data)

    A = data{1};
    B = data{2};

    C = zeros(4,16);
    C([14,24,31,33]) = 64*A(3)^2;
    C([3,5,38,48]) = -32*A(1)^2+32*A(2)^2;
    C([12,17,26,43]) = 32*A(1)^2+32*A(2)^2;
    C([56,59]) = -8*A(3)*B(5)-8*1i*A(3)*B(6);
    C([50,61]) = 8*1i*A(3)*B(6)-8*A(3)*B(5);
    C(60) = -8*A(1)*B(1)+8*A(2)*B(4)-8*1i*A(1)*B(2)-8*1i*A(2)*B(3);
    C(49) = -8*A(1)*B(1)+8*A(2)*B(4)+8*1i*A(1)*B(2)+8*1i*A(2)*B(3);
    C(55) = 8*A(1)*B(1)+8*A(2)*B(4)+8*1i*A(1)*B(2)-8*1i*A(2)*B(3);
    C(62) = -8*1i*A(1)*B(2)+8*1i*A(2)*B(3)+8*A(1)*B(1)+8*A(2)*B(4);

    C = C./repmat(sqrt(sum(C.*conj(C),2)),1,size(C,2));

    U = @(w1,x1,y1,z1)[x1*w1^2*y1,x1*y1^2*w1,x1*w1^2*z1,x1*y1*z1*w1,x1*z1^2*w1,y1*x1*w1,y1^2*x1,z1*x1*w1,z1*y1*x1,z1^2*x1,x1*y1,x1*z1,w1,y1,z1,1];
    dU = @(w1,x1,y1,z1)[[2*y1*x1*w1,y1^2*x1,2*z1*x1*w1,z1*y1*x1,z1^2*x1,x1*y1,0,x1*z1,0,0,0,0,1,0,0,0];[w1^2*y1,y1^2*w1,w1^2*z1,y1*z1*w1,z1^2*w1,y1*w1,y1^2,z1*w1,z1*y1,z1^2,y1,z1,0,0,0,0];[x1*w1^2,2*y1*x1*w1,0,z1*x1*w1,0,x1*w1,2*x1*y1,0,x1*z1,0,x1,0,0,1,0,0];[0,0,x1*w1^2,y1*x1*w1,2*z1*x1*w1,0,0,x1*w1,x1*y1,2*x1*z1,0,x1,0,0,1,0]];

end